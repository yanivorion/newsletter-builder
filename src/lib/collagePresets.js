export function getPresetById(id) {
  return collagePresets.find(p => p.id === id) || collagePresets[0];
}

export function getImageCountForPreset(preset) {
  if (!preset || !preset.preview) return 1;
  const uniqueIds = new Set();
  preset.preview.forEach(row => row.forEach(cell => uniqueIds.add(cell)));
  return uniqueIds.size;
}

export function getPresetsByCategory(category) {
  return collagePresets.filter(p => p.category === category);
}

