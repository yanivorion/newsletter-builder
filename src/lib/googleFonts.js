// Cache for loaded fonts
const loadedFonts = new Set();

// Load a Google Font dynamically
export function loadGoogleFont(fontName, weights = [400, 500, 600, 700]) {
  if (loadedFonts.has(fontName)) return;
  
  const font = GOOGLE_FONTS.find(f => f.name === fontName);
  const availableWeights = font ? font.weights : weights;
  
  // Create the Google Fonts URL
  const familyParam = `${fontName.replace(/ /g, '+')}:wght@${availableWeights.join(';')}`;
  const url = `https://fonts.googleapis.com/css2?family=${familyParam}&display=swap`;
  
  // Check if link already exists
  if (document.querySelector(`link[href="${url}"]`)) {
    loadedFonts.add(fontName);
    return;
  }
  
  // Create and append the link element
  const link = document.createElement('link');
  link.href = url;
  link.rel = 'stylesheet';
  document.head.appendChild(link);
  
  loadedFonts.add(fontName);
}

// Get font categories
export function getFontsByCategory(category) {
  return GOOGLE_FONTS.filter(f => f.category === category);
}

// Get all categories
export function getFontCategories() {
  return ['hebrew', 'sans-serif', 'serif', 'display', 'monospace'];
}

// Generate CSS font-family value
export function getFontStack(fontName) {
  const font = GOOGLE_FONTS.find(f => f.name === fontName);
  
  if (!font) return fontName;
  
  switch (font.category) {
    case 'hebrew':
      return `'${fontName}', 'Arial Hebrew', Arial, sans-serif`;
    case 'serif':
      return `'${fontName}', Georgia, 'Times New Roman', serif`;
    case 'monospace':
      return `'${fontName}', 'Courier New', monospace`;
    case 'display':
      return `'${fontName}', cursive, sans-serif`;
    default:
      return `'${fontName}', 'Helvetica Neue', Arial, sans-serif`;
  }
}

